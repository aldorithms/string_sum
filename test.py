import string_sum;

print("1 + 2 = " + string_sum.sum_as_string(1, 2)) 
print("1 - 3 = " + string_sum.difference_as_string(1, 3))
print("3 * 5 = " + string_sum.product_as_string(3, 5))
print("8 / 2 = " + string_sum.quotient_as_string(8, 2))
print("1 % 2 = " + string_sum.remainder_as_string(1, 2))
print("2 ^ 3 = " + string_sum.power_as_string(2, 3))

